
public class LoopDemo {

	public static void main(String[] args) {

		// For Loop

		for (int i = 1; i < 10; i += 2) { // tek sayıları çağır

			System.out.println(i);

		}

		System.out.println("For Dönügüsü Bitti");

		// While Loop

		int i = 1;

		while (i < 10) {
			
			System.out.println(i);
			i++;
			
		}
		System.out.println("While Döngüsü Bitti");
	}

}
